// Part Two
const countZeroHits = (pos, steps) => {
    const abs = Math.abs(steps)
    let firstZero = steps > 0 ? (100 - (pos % 100)) % 100 : pos % 100
    if (firstZero === 0) firstZero = 100
    return abs >= firstZero ? Math.floor((abs - firstZero) / 100) + 1 : 0
}

const partTwo = () => {
    let position = 50
    let password = 0
    for(const move of input) {
        const [dir, ...value ] = move
        const minus = dir === 'L'
        const steps = Number(value.join('')) * (minus ? -1 : 1)

        password += countZeroHits(position, steps)
        position = moveDial(position, steps)
    }

    return password
}

// Result
const t2 = performance.now();
console.log('Part Two', partTwo())
console.log(`Tiempo: ${performance.now() - t2} ms`);
